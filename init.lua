  local win_count = #vim.api.nvim_list_wins()

  if win_count == 1 then
    -- Only one window, so create a horizontal split and move to it
    vim.cmd 'vsplit'
    vim.cmd 'wincmd l'
  else
    -- Fallback: default behavior of <C-w>l (move to the right split)
